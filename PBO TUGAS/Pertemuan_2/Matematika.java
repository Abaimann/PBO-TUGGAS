package Pertemuan_2;


public class Matematika implements Method{
    
    public int Pertambahan(int a,int b){
        return a+b;
    };
    
    public int	Pengurangan(int  a, int b){
        return a-b;
    };
    public int	Perkalian(int  a, int  b){
        return a*b;
    };
    public double Pembagian(int  a, int  b){
        return (double) a/b;
    }; 
}
